package x7030.nefzi.tjinitaw.Model;

public class OthersModel {
    private String name,image;

    public OthersModel() {
    }

    public OthersModel(String name, String image) {
        this.name = name;
        this.image = image;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }
}
